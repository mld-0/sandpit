import numpy as np

values = np.arange(10)
print("values")
print(values)

n = 4
print("n")
print(n)

#   true_divide is equivelent to /
#   floor_divide is equivelent to //

result = np.true_divide(values, n)
print("true_divide")
print(result)


result = np.floor_divide(values, n)
print("floor_divide")
print(result)



